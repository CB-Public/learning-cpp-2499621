#include <iostream>

int main()
{
  // Anlegen und initialisieren der ersten Variable
  int meine_erste_variable = 4;

  // Anlegen und initialisieren der zweiten Variable
  int meine_zweite_variable = 2;

  // Berechnung der Summe
  int mein_ergebnis = meine_erste_variable + meine_zweite_variable;

  // Ausgabe des Ergebnisses
  std::cout << "Die Summe ist " << mein_ergebnis << std::endl;

  return 0;
}